#include "libft.h"

int			ft_power(int nb, int pw)
{
	if (pw == 0)
		return (1);
	if (pw == 1)
		return (nb);
	while (pw != 0)
	{
		nb = nb * nb;
		pw--;
	}
	return (nb);
}
