#include "libft.h"

void		*ft_memcpy(void *dst, const void *src, size_t n)
{
	int				i;
	unsigned char	*dst2;
	unsigned char	*src2;

	i = 0;
	dst2 = (unsigned char *)dst;
	src2 = (unsigned char *)src;
	while (n != 0)
	{
		dst2[i] = src2[i];
		i++;
		n--;
	}
	return (dst);
}
