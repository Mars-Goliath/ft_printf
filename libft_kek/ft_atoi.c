#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		y;
	int		n;

	i = 0;
	y = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == 45)
			n = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		y = y * 10 + str[i] - '0';
		i++;
	}
	if (n == 1)
		y = -y;
	return (y);
}
